public class Employee {

    public String surnameNamePatronymic;
    public String position;
    public String email;
    public int telephone;
    public int salary;
    public int age;

    public void Employee (String surnameNamePatronymic, String position, String email, int telephone, int salary, int age) {
       this.surnameNamePatronymic = surnameNamePatronymic;
       this.position = position;
       this.email = email;
       this.telephone = telephone;
       this.salary = salary;
       this.age = age;
    }

}
